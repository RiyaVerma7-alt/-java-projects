public class Manipulatenumber {
    public static void main(String[] args) {
          int a = 10;
          System.out.println(a);
          a += 3;
           System.out.println(a); 
           a -= 3;
           System.out.println(a); 
           a *= 3;
           System.out.println(a);
           a /= 3;
           System.out.println(a); 
           a %= 3;
           System.out.println(a);
    }
}
