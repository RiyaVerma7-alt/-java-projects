public class autoboxing {
    public static void main(String[] args) {
        int num1 = 20;
        System.out.println("primitive value"+ num1);
        int num2 = num1;
        System.out.println("after aoutboxing:"+ num2);
        int num3 = num2;
        System.out.println("after unboxing:"+ num3);
    }
}
